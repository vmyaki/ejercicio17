
public class main {

	public static void main(String[] args) {
		// Declaramos variables
					int num=50;
				
				//Bucle While
				System.out.println("Bucle con While");	
				while(num>=1)
				{
					System.out.println(num);
					num=num-1;
				}
				
				num=50;
				//Bucle Do While
				System.out.println("Bucle con Do While");
				do {

					System.out.println(num);
					num=num-1;
				}
				while(num>=1);
	}

}

